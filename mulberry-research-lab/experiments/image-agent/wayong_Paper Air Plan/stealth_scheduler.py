import time
import random

def human_like_typing_delay(text, min_delay=0.05, max_delay=0.2):
    """Simulates human-like typing delays between characters."""
    print(f"Typing: '{text}'")
    for char in text:
        print(char, end='', flush=True) # Print character immediately
        time.sleep(random.uniform(min_delay, max_delay))
    print()

def human_like_click_delay(min_delay=0.5, max_delay=1.5):
    """Simulates human-like mouse click delays."""
    delay = random.uniform(min_delay, max_delay)
    print(f"Simulating mouse click... waiting {delay:.2f} seconds.")
    time.sleep(delay)
    print("Click registered!")

