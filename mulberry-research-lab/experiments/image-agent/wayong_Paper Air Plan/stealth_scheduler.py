import time
import random

class HumanLikeScheduler:
    def __init__(self, speed_level='normal'):
        self.speed_level = speed_level.lower()
        self._set_delay_ranges()

    def _set_delay_ranges(self):
        # Define base means and standard deviations for 'normal' speed
        # Using a 3-sigma rule: range = mean +/- 3*std_dev
        # So, std_dev = (max - min) / 6, and mean = (min + max) / 2

        # For Typing (base min=0.05, max=0.2)
        base_typing_mean = (0.05 + 0.2) / 2  # 0.125
        base_typing_std = (0.2 - 0.05) / 6   # 0.025

        # For Clicking (base min=0.5, max=1.5)
        base_click_mean = (0.5 + 1.5) / 2    # 1.0
        base_click_std = (1.5 - 0.5) / 6     # 0.1667

        if self.speed_level == 'slow':
            self.typing_mean = base_typing_mean * 1.5
            self.typing_std = base_typing_std * 1.5
            self.click_mean = base_click_mean * 1.5
            self.click_std = base_click_std * 1.5
        elif self.speed_level == 'fast':
            self.typing_mean = base_typing_mean / 1.5
            self.typing_std = base_typing_std / 1.5
            self.click_mean = base_click_mean / 1.5
            self.click_std = base_click_std / 1.5
        else: # 'normal' or unrecognized
            self.typing_mean = base_typing_mean
            self.typing_std = base_typing_std
            self.click_mean = base_click_mean
            self.click_std = base_click_std

        print(f"Scheduler initialized with '{self.speed_level}' speed:")
        print(f"  Typing delays: Mean={self.typing_mean:.2f}s, StdDev={self.typing_std:.2f}s")
        print(f"  Click delays: Mean={self.click_mean:.2f}s, StdDev={self.click_std:.2f}s")

    def type_text(self, text):
        """Simulates human-like typing delays between characters based on scheduler speed using Gaussian distribution."""
        print(f"\nTyping (speed: {self.speed_level}): '{text}'")
        for char in text:
            print(char, end='', flush=True)
            # Ensure delay is not negative
            delay = max(0.001, random.gauss(self.typing_mean, self.typing_std))
            time.sleep(delay)
        print()

    def click(self, action_name="mouse click"):
        """Simulates human-like delays for a click action based on scheduler speed using Gaussian distribution."""
        # Ensure delay is not negative
        delay = max(0.001, random.gauss(self.click_mean, self.click_std))
        print(f"Simulating {action_name} (speed: {self.speed_level})... waiting {delay:.2f} seconds.")
        time.sleep(delay)
        print(f"{action_name.capitalize()} registered!")

